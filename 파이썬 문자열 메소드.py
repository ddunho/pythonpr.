letter='hELLO? lET ME INTRODUCE MY SELF. mY NAME IS sEUNGMIN jUNG.i AM 15 YEARS OLD. tHERE ARE...'
print(letter.lower()) #모든 글자를 소문자로, 반대는 upper
print(letter.capitalize()) #첫 글자만 대문자로, 나머지는 모두 소문자
print(letter.swapcase()) #모든 문장에서의 대문자와 소문자를 뒤바꿔줌
print(letter.title()) #각 단어들의 첫글자만 대문자로
print(letter.split()) #띄어쓰기로 나누어진 단어를 리스트형태로 반환