python='파이썬' 
java='자바'
print('개발 언어에는 {},{} 등이 있어요'.format(python,java)) 
"""변수 선언 방법
   1. python,java = 파이썬,자바
   2. python='파이썬'
   java='자바'
   """
print('개발 언어에는',python,java,'등이 있어요')

print('개발 언어에는 {0},{1} 등이 있어요'.format(python,java))
#순서대로 변수가 들어감

print(f'개발 언어에는 {python},{java} 등이 있어요')
#개인적으로 가장 편리
